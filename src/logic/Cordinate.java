package logic;

public class Cordinate {
private int x; //x cordinate
private int y; //y cordinate
public int getX() {
	return x;
}
public void setX(int x) {
	this.x = x;
}
public int getY() {
	return y;
}
public void setY(int y) {
	this.y = y;
}
public Cordinate(int x, int y) {
	super();
	this.x = x;
	this.y = y;
}

}
